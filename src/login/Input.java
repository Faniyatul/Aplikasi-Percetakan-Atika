    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        Connection koneksi = null;
        
        try {
        Class.forName ("com.mysql.jdbc.Driver");
        koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/form input","root","");
    } catch (Exception e) {
     JOptionPane.showMessageDialog(null, "Koneksi Database Gagal.\n" + e);
    }
    Statement insert;
    String nomer = nomerpesanan.getText();
    String nama = namaa.getText();
    String telp = telpon.getText();
    String pemesanan = tglpemesanan.getText();
    String pengambilan = tglpengambilan.getText();
    String jenis = jenispes.getText();
    String jumlah = jumlahh.getText();
    String harga = hargaa.getText();
    String total = totall.getText();
    String dp = jumlahdp.getText();
    
    try {
        insert = koneksi.createStatement();
        String query = ""
                + "INSERT INTO input"
                + "("
                    + "nomer,"
                    + "nama,"
                    + "telp,"
                    + "pemesanan,"
                    + "pengambilan,"
                    + "jenis,"
                    + "jumlah,"
                    + "harga,"
                    + "total,"
                    + "dp"
                + ")"
                + "VALUES"
                + "("
                    + "'"+ nomer + "',"
                    + "'"+ nama + "',"
                    + "'"+ telp + "',"
                    + "'"+ pemesanan + "',"
                    + "'"+ pengambilan + "',"
                    + "'"+ jenis + "',"
                    + "'"+ jumlah + "',"
                    + "'"+ harga + "',"
                    + "'"+ total + "',"
                    + "'"+ dp + "'"
                + ");";
        insert.executeUpdate(query);
        JOptionPane.showMessageDialog(null, "Data Tersimpan!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Input Gagal.\n" + e);
        
    }
    }//GEN-LAST:event_simpanActionPerformed

